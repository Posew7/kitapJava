
public class WhileDongusu {

    public static void main(String[] args) {

        int sayi = 5;

        while (sayi >= 0) {
            System.out.println("5 kere döndüreceğiz");
            sayi--;
        }

        int abakus = 0;
        
        while (abakus <= 10) {
            System.out.println("abakus = " + abakus);
            abakus++;
        }
        System.out.println("abakus doğru sayıyor");
    }
}
