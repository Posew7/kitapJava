
public class IfElseYapisi {

    public static void main(String[] args) {

        int birinciSayi = 15;
        int ikinciSayi = 25;

        if (birinciSayi == ikinciSayi) {
            System.out.println("birinci sayı ikinci sayıya eşittir");
        } else {
            System.out.println("birinci sayı ikinci sayıdan küçüktür");
        }
    }
}
