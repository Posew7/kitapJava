
public class DoWhileDongusu {

    public static void main(String[] args) {

        int sayi = 0;

        do {
            System.out.println("sayi = " + sayi);
        } while (sayi < 0);

        int sayac = 1;

        do {
            System.out.println("ben bir do while döngüsüyüm");
            sayac++;
        } while (sayac <= 0);
        int sayac2 = 1;
        do {
            System.out.println("ben bir do while döngüsüyüm");
            sayac2++;
        } while (sayac2 <= 5);

    }
}
